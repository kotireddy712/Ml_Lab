class Node:
    def __init__(self, feature=None, threshold=None, info_gain=None, left=None, right=None, value=None):

        # Decision node attributes
        self.feature = feature
        self.threshold = threshold
        self.info_gain = info_gain
        self.left = left
        self.right = right

        # Leaf node attribute
        self.value = value

