class DecisionTree:
    def __init__(self, min_samples=2, max_depth=2):
        self.min_samples = min_samples
        self.max_depth = max_depth

    def build_tree(self, dataset, curr_depth=0):
        X = dataset[:, :-1]
        y = dataset[:, -1]
        n_samples, n_features = X.shape

        if n_samples >= self.min_samples and curr_depth <= self.max_depth:
            best_split = self.best_split(dataset, n_features)

            if best_split["info_gain"] > 0:
                left_node = self.build_tree(
                    best_split["left_dataset"], curr_depth+1)
                right_node = self.build_tree(
                    best_split["right_dataset"], curr_depth+1)

                return Node(best_split["feature"], best_split["threshold"], best_split["info_gain"], left_node, right_node)
        leaf_value = Counter(y).most_common(1)[0][0]
        return Node(value=leaf_value)

    def best_split(self, dataset, n_features):
        best_split = {'feature': None, 'threshold': None,
                      'info_gain': -1, 'left_dataset': None, 'right_dataset': None}

        for feature in range(n_features):
            feature_values = dataset[:, feature]
            thresholds = np.unique(feature_values)

            for threshold in thresholds:
                left_dataset, right_dataset = self.split(
                    dataset, feature, threshold)

                if len(left_dataset) and len(right_dataset):
                    parent_y, left_y, right_y = dataset[:, -
                                                        1], left_dataset[:, -1], right_dataset[:, -1]
                    info_gain = self.information_gain(
                        parent_y, left_y, right_y)

                    if info_gain > best_split['info_gain']:
                        best_split['feature'] = feature
                        best_split['threshold'] = threshold
                        best_split['info_gain'] = info_gain
                        best_split['left_dataset'] = left_dataset
                        best_split['right_dataset'] = right_dataset
        return best_split

    def split(self, dataset, feature, threshold):
        left_dataset = np.array(
            [row for row in dataset if row[feature] <= threshold])
        right_dataset = np.array(
            [row for row in dataset if row[feature] > threshold])

        return left_dataset, right_dataset

    def information_gain(self, parent_y, left_y, right_y):
        left_weight = len(left_y) / len(parent_y)
        right_weight = len(right_y) / len(parent_y)

        information_gain = self.entropy(
            parent_y) - (left_weight * self.entropy(left_y) + right_weight * self.entropy(right_y))
        return information_gain

    def entropy(self, y):
        entropy = 0
        class_labels = np.unique(y)
        for class_label in class_labels:
            p = len(y[y == class_label]) / len(y)
            entropy += -p * np.log2(p)

        return entropy

    def fit(self, X, y):
        dataset = np.concatenate([X, y.reshape(-1, 1)], axis=1)
        self.root = self.build_tree(dataset)

    # def predict(self, x):
