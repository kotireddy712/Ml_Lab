class RandomForest:
    def __init__(self, n_trees=10, max_depth=5, min_samples=2, max_features=None):
        self.n_trees = n_trees
        self.max_depth = max_depth
        self.min_samples = min_samples
        self.max_features = max_features
        self.trees = []

    def bootstrap_sample(self, X, y):
        n_samples = X.shape[0]
        idxs = np.random.choice(n_samples, n_samples, replace=True)
        return X[idxs], y[idxs]

    def fit(self, X, y):
        self.trees = []

        for _ in range(self.n_trees):
            X_sample, y_sample = self.bootstrap_sample(X, y)

            if self.max_features:
                feature_idxs = np.random.choice(
                    X.shape[1], self.max_features, replace=False
                )
            else:
                feature_idxs = np.arange(X.shape[1])

            tree = DecisionTree(
                max_depth=self.max_depth,
                min_samples=self.min_samples
            )

            tree.fit(X_sample[:, feature_idxs], y_sample)
            self.trees.append((tree, feature_idxs))

    def predict(self, X):
        tree_preds = []

        for tree, feature_idxs in self.trees:
            preds = tree.predict(X[:, feature_idxs])
            tree_preds.append(preds)

        tree_preds = np.array(tree_preds).T

        return [Counter(row).most_common(1)[0][0] for row in tree_preds]


