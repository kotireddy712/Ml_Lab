    #     return predictions
    def predict(self, x):
        predictions = []
        for row in x:
            pred = self.predict_class(row, self.root)
            predictions.append(pred)
        return predictions


    def predict_class(self, row, node):
        if node.value != None:
            return node.value

        feature = row[node.feature]
        if feature <= node.threshold:
            return self.predict_class(row, node.left)
        else:
            return self.predict_class(row, node.right)

    def print_tree(self, node=None, depth=0, indent="|    "):
        prefix = indent * depth
        if node is None:
            node = self.root

        if node.value is not None:
            print(f"{prefix}|--- Class: {node.value}")
            return
        feature_label = f"feature {node.feature}"
        print(
            f"{prefix}|--- {feature_label} <= {node.threshold}, Info Gain={node.info_gain:.4f}")
        self.print_tree(node.left, depth + 1, indent)

        print(
            f"{prefix}|--- {feature_label} > {node.threshold}, Info Gain={node.info_gain:.4f}")
        self.print_tree(node.right, depth + 1, indent)

