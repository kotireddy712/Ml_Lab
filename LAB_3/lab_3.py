def logistic_gd_l2(X, y, lr=0.01, epochs=1000, lam=0.1):
    m, n = X.shape
    y = y.reshape(-1, 1)
    w = np.zeros((n, 1))
    losses = []
    for _ in range(epochs):
        y_hat = sigmoid(np.dot(X, w))
        # L2: do NOT regularize bias term (w[0])
        dw = (1/m) * np.dot(X.T, (y_hat - y))
        dw[1:] += (lam / m) * w[1:]
        w -= lr * dw
        loss = compute_loss(y, y_hat) + (lam / (2*m)) * np.sum(w[1:]**2)
        losses.append(loss)

    return w, losses

lambdas = [0, 0.001, 0.01, 0.1, 1]
