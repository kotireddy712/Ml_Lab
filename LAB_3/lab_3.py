def sigmoid(z):
    z = np.clip(z, -500, 500) # to avoid over-range values.
    return 1 / (1 + np.exp(-z))

def compute_loss(y, y_hat):
    eps = 1e-9
    return -np.mean(y * np.log(y_hat + eps) + (1 - y) * np.log(1 - y_hat + eps))

def logistic_gd(X, y, lr=0.001, epochs=1000):
    m, n = X.shape
    y = y.reshape(-1, 1) # No needed, we done before above: but not harm any..
    # add bias column using np.c_
    # X = np.c_[np.ones((m, 1)), X]   # shape: (m, n+1)
    w = np.zeros((n, 1))
    losses = []
    for _ in range(epochs):
        z = np.dot(X, w)
        y_hat = sigmoid(z)
        dw = (1/m) * np.dot(X.T, (y_hat - y))
        w -= lr * dw
        losses.append(compute_loss(y, y_hat))

    return w, losses

def logistic_sgd(X, y, lr=0.01, epochs=10):
    m, n = X.shape
    y = y.reshape(-1, 1)
    # add bias column
    # X = np.c_[np.ones((m, 1)), X]   # (m, n+1)
    w = np.zeros((n, 1))
    losses = []
    for _ in range(epochs):
        for i in range(m):
            xi = X[i].reshape(1, -1)   # (1, n+1)
            yi = y[i]                  # (1,)
            y_hat = sigmoid(np.dot(xi, w))  # (1,1)
            dw = xi.T * (y_hat - yi)
            w -= lr * dw
        losses.append(compute_loss(y, sigmoid(np.dot(X, w))))

    return w, losses

def predict(X, w, threshold=0.5):
    m = X.shape[0]
    # add bias column
    # X = np.c_[np.ones((m, 1)), X]
    probs = sigmoid(np.dot(X, w))
    return (probs >= threshold).astype(int)

